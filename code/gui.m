% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
%% 控件参数的读取导入
Fig1_1 = handles.axes1;Fig1_2 = handles.axes2;Fig1_3 = handles.axes3;
Fig2_1 = handles.axes4;Fig2_2 = handles.axes5;Fig2_3 = handles.axes6;Fig2_4 = handles.axes7;
Fig3 = handles.axes10;
t0 = str2num(get(handles.edit1,'String'));%概率阈值
n0 = str2num(get(handles.edit2,'String'));%判断稳定性的阈值
WaveletMethod = get(handles.edit3,'String');%采用小波变换的方法（默认）
alpha = str2num(get(handles.edit4,'String'));%alpha的赋值
ts = get(handles.checkbox1,'value');%是否使用学习阈值
file=get(handles.edit5,'String');%文件名
FuncChoose = get(handles.popupmenu1,'value');%是否使用预测及那种预测

HR = get(handles.radiobutton1,'Value');%选哪个模型运行Holt_Winters/RBF
%% 读取数据并画图
FilePath = ['../data/',file];
DataNew = xlsread(FilePath);
SourceData = DataNew(1:400);
t = 1:length(SourceData);
plot(Fig1_1,t,SourceData);title(Fig1_1,'原始数据统计图');

[DataClass,ErrorList1] = AbnormalAnalysis(SourceData,ts,t0,n0,WaveletMethod,Fig1_2,Fig1_3);

if(HR)
    ErrorList2 = Holt_Winters(SourceData,alpha,Fig2_1,Fig2_2,Fig2_3,Fig2_4);
else
    ErrorList2 = RBF(SourceData,Fig2_1,Fig2_2,Fig2_3,Fig2_4);
end

Predict = dataforecast(SourceData,FuncChoose,Fig3);
decisiontree(DataClass,ts,t0,n0,WaveletMethod,alpha,ErrorList1,ErrorList2,FuncChoose,Predict,HR);





